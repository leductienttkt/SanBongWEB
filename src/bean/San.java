package bean;

public class San {

	public int idSan;
	public NguoiDung chuSan;
	public int gia;
	public int loai;
	public String ten;
	
	public int getIdSan() {
		return idSan;
	}
	public void setIdSan(int idSan) {
		this.idSan = idSan;
	}
	public NguoiDung getChuSan() {
		return chuSan;
	}
	public void setChuSan(NguoiDung chuSan) {
		this.chuSan = chuSan;
	}
	public int getGia() {
		return gia;
	}
	public void setGia(int gia) {
		this.gia = gia;
	}
	public int getLoai() {
		return loai;
	}
	public void setLoai(int loai) {
		this.loai = loai;
	}
	public String getTen() {
		return ten;
	}
	public void setTen(String ten) {
		this.ten = ten;
	}	
}
