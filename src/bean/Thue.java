package bean;

public class Thue {

	public int idThue;
	public San san;
	public NguoiDung nguoiThue;
	public int gioBatDau;
	public int gioKetThuc;
	public String ngay;
	
	public int getIdThue() {
		return idThue;
	}
	public void setIdThue(int idThue) {
		this.idThue = idThue;
	}
	public San getSan() {
		return san;
	}
	public void setSan(San san) {
		this.san = san;
	}
	public NguoiDung getNguoiThue() {
		return nguoiThue;
	}
	public void setNguoiThue(NguoiDung nguoiThue) {
		this.nguoiThue = nguoiThue;
	}
	public int getGioBatDau() {
		return gioBatDau;
	}
	public void setGioBatDau(int gioBatDau) {
		this.gioBatDau = gioBatDau;
	}
	public int getGioKetThuc() {
		return gioKetThuc;
	}
	public void setGioKetThuc(int gioKetThuc) {
		this.gioKetThuc = gioKetThuc;
	}
	public String getNgay() {
		return ngay;
	}
	public void setNgay(String ngay) {
		this.ngay = ngay;
	}
	
}
